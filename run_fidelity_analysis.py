def main():
    random.seed(SEED)
    np.random.seed(SEED)
    warnings.filterwarnings("ignore", category=RuntimeWarning)

    os.makedirs(OUTPUT_DIR, exist_ok=True)

    # -----------------------------------------------------------------------
    # 1. Load Intel data
    # -----------------------------------------------------------------------
    intel_df = load_intel_data()

    intel_temp = intel_df["temperature"].values
    intel_hum = intel_df["humidity"].values
    intel_light = intel_df["light"].values
    intel_volt = intel_df["voltage"].values

    # -----------------------------------------------------------------------
    # 2. Generate STGen synthetic traces
    # -----------------------------------------------------------------------
    print("[2/7] Generating STGen synthetic traces …")
    stgen_temp, stgen_hum = generate_mvou_temp_hum(N_STGEN_SAMPLES, motes=N_STGEN_MOTES)
    stgen_light = generate_stgen_light(N_STGEN_SAMPLES, motes=N_STGEN_MOTES)
    stgen_volt = generate_stgen_voltage(N_STGEN_SAMPLES, motes=N_STGEN_MOTES)
    print(f"    Generated {N_STGEN_SAMPLES:,} samples per modality")

    # -----------------------------------------------------------------------
    # 3. Marginal distribution statistics
    # -----------------------------------------------------------------------
    print("[3/7] Computing marginal distribution statistics …")
    results = {}
    modalities = {
        "temperature": (intel_temp, stgen_temp, "°C"),
        "humidity": (intel_hum, stgen_hum, "%"),
        "light": (intel_light, stgen_light, "lux"),
        "voltage": (intel_volt, stgen_volt, "V"),
    }

    for mod, (real, synth, unit) in modalities.items():
        results[mod] = {
            "intel": compute_moments(real),
            "stgen": compute_moments(synth),
            "ks": ks_test(real, synth),
            "jsd": js_divergence(real, synth),
            "unit": unit,
        }

    # -----------------------------------------------------------------------
    # 4. Temporal autocorrelation
    # -----------------------------------------------------------------------
    print("[4/7] Computing temporal autocorrelation (ACF) …")

    # Intel ACF: pick a high-data mote, sorted by timestamp
    for mod, col in [("temperature", "temperature"), ("humidity", "humidity"),
                     ("light", "light"), ("voltage", "voltage")]:
        # Use mote with most data for clean ACF
        mote_counts = intel_df.groupby("moteid")[col].count()
        best_mote = mote_counts.idxmax()
        mote_series = intel_df[intel_df["moteid"] == best_mote].sort_values(["date", "time"])[col].values

        results[mod]["intel_acf"] = compute_acf_lags(mote_series, ACF_LAGS)

    # STGen ACF: generate long single-mote MVOU traces
    N_ACF = STGEN_DURATION_STEPS
    rho_acf = -0.38
    theta_T_acf, sigma_T_acf = 0.002, 0.15
    theta_H_acf, sigma_H_acf = 0.001, 0.20

    # Temperature + Humidity via Cholesky-correlated noise
    stgen_temp_series = np.zeros(N_ACF)
    stgen_hum_series = np.zeros(N_ACF)
    mean_t = 22.0
    current_t = 22.0
    mean_h = 53.8 - 0.64 * mean_t
    current_h = mean_h
    dW1_acf = np.random.normal(0, 1.0, N_ACF)
    dW2_acf = np.random.normal(0, 1.0, N_ACF)
    dZ_T_acf = dW1_acf
    dZ_H_acf = rho_acf * dW1_acf + math.sqrt(1 - rho_acf**2) * dW2_acf
    for i in range(N_ACF):
        if random.random() < 0.001:
            mean_t += random.gauss(0, 4)
            mean_t = max(15, min(40, mean_t))
            mean_h = 53.8 - 0.64 * mean_t
        current_t += theta_T_acf * (mean_t - current_t) + sigma_T_acf * dZ_T_acf[i]
        current_h += theta_H_acf * (mean_h - current_h) + sigma_H_acf * dZ_H_acf[i]
        current_h = max(0.0, min(100.0, current_h))
        stgen_temp_series[i] = current_t
        stgen_hum_series[i] = current_h
    results["temperature"]["stgen_acf"] = compute_acf_lags(stgen_temp_series, ACF_LAGS)
    results["humidity"]["stgen_acf"] = compute_acf_lags(stgen_hum_series, ACF_LAGS)

    # Light: bimodal OU with regime switching (increased bright sigma)
    stgen_light_series = np.zeros(N_ACF)
    bright = random.random() > 0.375
    l_current = 639.0 if bright else 28.0
    for i in range(N_ACF):
        if bright:
            if random.random() < 0.012: bright = False
            target_l, sig_l = 639.0, 80.0
        else:
            if random.random() < 0.02: bright = True
            target_l, sig_l = 28.0, 5.0
        l_current = _ou_step(l_current, target_l, theta=0.01, sigma=sig_l,
                             lo=0, hi=100000)
        stgen_light_series[i] = l_current
    results["light"]["stgen_acf"] = compute_acf_lags(stgen_light_series, ACF_LAGS)

    # Voltage: OU with slow discharge
    stgen_volt_series = np.zeros(N_ACF)
    v_mean = 2.56
    v_current = 2.56
    for i in range(N_ACF):
        v_mean -= 0.00001
        v_current = _ou_step(v_current, v_mean, theta=0.001, sigma=0.005,
                             lo=1.5, hi=3.5)
        stgen_volt_series[i] = v_current
    results["voltage"]["stgen_acf"] = compute_acf_lags(stgen_volt_series, ACF_LAGS)

    # -----------------------------------------------------------------------
    # 5. Cross-correlation: temperature–humidity
    # -----------------------------------------------------------------------
    print("[5/7] Computing cross-correlations …")
    cross_corr = {
        "intel": compute_cross_corr(intel_temp, intel_hum),
        "stgen": compute_cross_corr(stgen_temp, stgen_hum),
    }

    # -----------------------------------------------------------------------
    # 6. Inter-arrival time comparison
    # -----------------------------------------------------------------------
    print("[6/7] Computing inter-arrival time statistics …")
    intel_iat = compute_iat_stats(intel_df)
    # STGen nominal: Intel-referenced 31s interval (for fidelity comparison only)
    stgen_iat_values = np.full(10000, INTEL_SAMPLE_INTERVAL_SEC)
    stgen_iat_weibull = np.array([INTEL_SAMPLE_INTERVAL_SEC * ((-math.log(max(random.random(), 1e-10))) ** (1.0 / 0.8))
                                   for _ in range(10000)])
    iat_comparison = {
        "intel_mean": intel_iat["mean"],
        "intel_std": intel_iat["std"],
        "stgen_mean": INTEL_SAMPLE_INTERVAL_SEC,
        "stgen_std": 0.0,
        "stgen_weibull_mean": float(np.mean(stgen_iat_weibull)),
        "stgen_weibull_std": float(np.std(stgen_iat_weibull)),
    }

    # -----------------------------------------------------------------------
    # 7. Per-mote variability
    # -----------------------------------------------------------------------
    print("[7/7] Computing per-mote variability …")
    mote_cv = {}
    for col in ["temperature", "humidity", "light", "voltage"]:
        mote_cv[col] = per_mote_cv(intel_df, col)

    # ===================================================================
    # OUTPUT: Console summary
    # ===================================================================
    print("\n" + "=" * 80)
    print("CALIBRATION & FIDELITY ANALYSIS: STGen vs Intel Berkeley Lab")
    print("=" * 80)

    # Marginal summary
    print("\n┌─────────────────────────────────────────────────────────────────────────────┐")
    print("│  MARGINAL DISTRIBUTION COMPARISON                                           │")
    print("├───────────────┬──────────────────────┬──────────────────────┬────────┬───────┤")
    print("│  Modality     │  Intel Lab (μ ± σ)   │  STGen (μ ± σ)       │  KS D  │  JSD  │")
    print("├───────────────┼──────────────────────┼──────────────────────┼────────┼───────┤")
    for mod in ["temperature", "humidity", "light", "voltage"]:
        r = results[mod]
        name = mod.capitalize().ljust(13)
        intel_str = f"{r['intel']['mean']:7.2f} ± {r['intel']['std']:6.2f}".ljust(20)
        stgen_str = f"{r['stgen']['mean']:7.2f} ± {r['stgen']['std']:6.2f}".ljust(20)
        ks_str = f"{r['ks']['D']:.4f}"
        jsd_str = f"{r['jsd']:.4f}"
        print(f"│  {name} │  {intel_str} │  {stgen_str} │ {ks_str} │{jsd_str} │")
    print("└───────────────┴──────────────────────┴──────────────────────┴────────┴───────┘")

    # ACF summary
    print("\n┌──────────────────────────────────────────────────────────────────────────────┐")
    print("│  AUTOCORRELATION (ACF) AT KEY LAGS                                           │")
    print("├───────────────┬────────────────────────────┬─────────────────────────────────┤")
    lag_hdr = "  ".join([f"L{l:>2}" for l in ACF_LAGS])
    print(f"│  Modality     │  Intel: {lag_hdr}   │  STGen: {lag_hdr}   │")
    print("├───────────────┼────────────────────────────┼─────────────────────────────────┤")
    for mod in ["temperature", "humidity", "light", "voltage"]:
        r = results[mod]
        name = mod.capitalize().ljust(13)
        intel_acf_str = "  ".join([f"{r['intel_acf'].get(l, float('nan')):5.3f}" for l in ACF_LAGS])
        stgen_acf_str = "  ".join([f"{r['stgen_acf'].get(l, float('nan')):5.3f}" for l in ACF_LAGS])
        print(f"│  {name} │  {intel_acf_str} │  {stgen_acf_str}  │")
    print("└───────────────┴────────────────────────────┴─────────────────────────────────┘")

    # Cross-correlation
    print(f"\n  Temperature–Humidity Cross-Correlation:")
    print(f"    Intel Lab:  r = {cross_corr['intel']:.4f}")
    print(f"    STGen:      r = {cross_corr['stgen']:.4f}")

    # IAT
    print(f"\n  Inter-Arrival Time:")
    print(f"    Intel Lab:  μ = {iat_comparison['intel_mean']:.1f}s, σ = {iat_comparison['intel_std']:.1f}s")
    print(f"    STGen (fixed):  μ = {iat_comparison['stgen_mean']:.1f}s, σ = {iat_comparison['stgen_std']:.1f}s")
    print(f"    STGen (Weibull k=0.8): μ = {iat_comparison['stgen_weibull_mean']:.1f}s, σ = {iat_comparison['stgen_weibull_std']:.1f}s")

    # Per-mote variability
    print("\n  Per-Mote Coefficient of Variation (Intel Lab):")
    for col in ["temperature", "humidity", "light", "voltage"]:
        cv = mote_cv[col]
        print(f"    {col.capitalize():13s}: mean CV = {cv['mean_cv']:.3f} (range {cv['min_cv']:.3f}–{cv['max_cv']:.3f})")

    # ===================================================================
    # OUTPUT: Figures
    # ===================================================================
    print(f"\nGenerating figures in {OUTPUT_DIR}/ …")

    for mod, (real, synth, unit) in modalities.items():
        plot_distribution_comparison(
            real, synth, mod.capitalize(), unit,
            OUTPUT_DIR / f"dist_{mod}.png"
        )

    for mod in ["temperature", "humidity", "light", "voltage"]:
        r = results[mod]
        plot_acf_comparison(
            r["intel_acf"], r["stgen_acf"], mod.capitalize(),
            OUTPUT_DIR / f"acf_{mod}.png"
        )

    plot_iat_comparison(
        intel_iat["values"], stgen_iat_weibull,
        OUTPUT_DIR / "iat_comparison.png"
    )

    plot_cross_correlation(
        intel_temp, intel_hum, stgen_temp, stgen_hum,
        OUTPUT_DIR / "cross_corr_temp_hum.png"
    )

    plot_summary_dashboard(results, OUTPUT_DIR / "fidelity_dashboard.png")

    # ===================================================================
    # OUTPUT: LaTeX tables
    # ===================================================================
    generate_latex_tables(results, cross_corr, iat_comparison,
                          OUTPUT_DIR / "fidelity_tables.tex")
    print(f"  LaTeX tables → {OUTPUT_DIR / 'fidelity_tables.tex'}")

    # ===================================================================
    # OUTPUT: JSON results
    # ===================================================================
    json_results = {
        "marginal": {},
        "acf": {},
        "cross_correlation": cross_corr,
        "inter_arrival_time": {k: v for k, v in iat_comparison.items()},
        "per_mote_cv": mote_cv,
    }
    for mod in ["temperature", "humidity", "light", "voltage"]:
        r = results[mod]
        json_results["marginal"][mod] = {
            "intel": r["intel"],
            "stgen": r["stgen"],
            "ks_D": r["ks"]["D"],
            "ks_p": r["ks"]["p"],
            "jsd": r["jsd"],
        }
        json_results["acf"][mod] = {
            "intel": {str(k): v for k, v in r["intel_acf"].items()},
            "stgen": {str(k): v for k, v in r["stgen_acf"].items()},
        }

    with open(OUTPUT_DIR / "fidelity_results.json", "w") as f:
        json.dump(json_results, f, indent=2)
    print(f"  JSON results → {OUTPUT_DIR / 'fidelity_results.json'}")

    # ===================================================================
    # Fidelity Score Summary
    # ===================================================================
    print("\n" + "=" * 80)
    print("FIDELITY SCORECARD")
    print("=" * 80)
    overall_scores = []
    for mod in ["temperature", "humidity", "light", "voltage"]:
        r = results[mod]
        # Score: 1 - KS_D (closer to 1 = better match)
        ks_score = 1.0 - r["ks"]["D"]
        # Score: 1 - JSD (closer to 1 = better match)
        jsd_score = 1.0 - min(r["jsd"], 1.0)
        # ACF similarity: 1 - mean |ACF_intel - ACF_stgen| across lags
        acf_diffs = []
        for l in ACF_LAGS:
            i_acf = r["intel_acf"].get(l, 0)
            s_acf = r["stgen_acf"].get(l, 0)
            if not (math.isnan(i_acf) or math.isnan(s_acf)):
                acf_diffs.append(abs(i_acf - s_acf))
        acf_score = 1.0 - (np.mean(acf_diffs) if acf_diffs else 0.5)

        composite = (ks_score + jsd_score + acf_score) / 3.0
        overall_scores.append(composite)
        print(f"  {mod.capitalize():13s}:  KS={ks_score:.3f}  JSD={jsd_score:.3f}  ACF={acf_score:.3f}  → Composite={composite:.3f}")

    print(f"\n  Overall Fidelity Score (Default): {np.mean(overall_scores):.3f} / 1.000")
    print("=" * 80)

    # ===================================================================
    # CALIBRATED MODEL: fit to Intel data, re-generate, compare
    # ===================================================================
    if RUN_CALIBRATED:
        print("\n" + "=" * 80)
        print("PHASE 2: CALIBRATED MODEL (parameters fitted to Intel data)")
        print("=" * 80)

        # --- Calibrate OU temperature ---
        print("\n  Fitting OU parameters from Intel temperature …")
        mote_counts = intel_df.groupby("moteid")["temperature"].count()
        best_mote = mote_counts.idxmax()
        best_series = intel_df[intel_df["moteid"] == best_mote].sort_values(
            ["date", "time"])["temperature"].values
        theta_cal, mu_cal, sigma_cal = calibrate_ou_params(best_series)
        print(f"    θ = {theta_cal:.4f},  μ = {mu_cal:.2f}°C,  σ = {sigma_cal:.4f}")

        # --- Calibrate humidity ---
        print("  Fitting humidity model …")
        a_hum, b_hum, noise_hum = calibrate_humidity_model(intel_temp, intel_hum)
        print(f"    h = {a_hum:.2f} + {b_hum:.4f}·T,  noise σ = {noise_hum:.2f}")

        # --- Calibrate light ---
        print("  Fitting light mixture model …")
        mu1_l, sig1_l, w_dark, mu2_l, sig2_l = calibrate_light_model(intel_light)
        print(f"    Dark mode:  μ₁={mu1_l:.1f}, σ₁={sig1_l:.1f}, w={w_dark:.3f}")
        print(f"    Bright mode: μ₂={mu2_l:.1f}, σ₂={sig2_l:.1f}")

        # --- Calibrate voltage ---
        volt_mu = float(np.mean(intel_volt))
        volt_sig = float(np.std(intel_volt))
        print(f"  Voltage calibrated: μ = {volt_mu:.3f}V,  σ = {volt_sig:.3f}V")

        # --- Generate calibrated traces ---
        print("\n  Generating calibrated STGen traces …")
        np.random.seed(SEED)
        random.seed(SEED)
        cal_temp, cal_hum = generate_calibrated_mvou_temp_hum(
            N_STGEN_SAMPLES, N_STGEN_MOTES, theta_cal, sigma_cal,
            a_hum, b_hum, noise_hum, intel_temp)
        cal_light = generate_calibrated_light(
            N_STGEN_SAMPLES, mu1_l, sig1_l, w_dark, mu2_l, sig2_l)
        cal_volt = generate_calibrated_voltage(N_STGEN_SAMPLES, intel_volt)

        # --- Compute calibrated metrics ---
        cal_results = {}
        cal_modalities = {
            "temperature": (intel_temp, cal_temp, "°C"),
            "humidity": (intel_hum, cal_hum, "%"),
            "light": (intel_light, cal_light, "lux"),
            "voltage": (intel_volt, cal_volt, "V"),
        }
        for mod, (real, synth, unit) in cal_modalities.items():
            cal_results[mod] = {
                "intel": compute_moments(real),
                "stgen_cal": compute_moments(synth),
                "ks": ks_test(real, synth),
                "jsd": js_divergence(real, synth),
                "unit": unit,
            }

        # Calibrated ACF: MVOU with Cholesky-correlated noise
        rho_cal = -0.38
        theta_H_cal = 0.001
        sigma_H_cal = noise_hum * 0.05
        cal_temp_series = np.zeros(STGEN_DURATION_STEPS)
        cal_hum_series = np.zeros(STGEN_DURATION_STEPS)
        ct = mu_cal
        ch = a_hum + b_hum * mu_cal
        dW1_cal = np.random.normal(0, 1.0, STGEN_DURATION_STEPS)
