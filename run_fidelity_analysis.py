def calibrate_ou_params(series: np.ndarray, dt: float = 1.0):
    """Estimate OU process parameters (theta, mu, sigma) from a time series.
    
    Uses method-of-moments:
      mu = sample mean
            Auto-correlation at lag-1: rho_1 = exp(-theta*dt)
        → theta = -ln(rho_1) / dt
      sigma = std(residuals) * sqrt(2*theta)

        Note:
            dt is in model step units. In this script, one step corresponds to one
            Intel sample interval (~31s), so default dt=1.0 is intentional.
    """
    mu = float(np.mean(series))
    # Lag-1 autocorrelation
    centered = series - mu
    if len(centered) < 3:
        return 0.1, mu, 0.5  # fallback
    rho1 = np.corrcoef(centered[:-1], centered[1:])[0, 1]
    rho1 = max(rho1, 0.001)  # avoid log(0)
    theta = -math.log(rho1) / dt
    theta = max(theta, 0.001)
    # Residual volatility
    residuals = centered[1:] - rho1 * centered[:-1]
    sig_res = float(np.std(residuals))
    sigma = sig_res * math.sqrt(2 * theta)
    return theta, mu, sigma


def ou_temperature_calibrated(current, mean, dt, theta, sigma):
    """Calibrated OU temperature step."""
    dW = random.gauss(0, 1) * math.sqrt(dt)
    dT = theta * (mean - current) * dt + sigma * dW
    max_rate = 0.5 / 60
    dT = max(-max_rate * dt, min(max_rate * dt, dT))
    return current + dT


def calibrate_humidity_model(intel_temp, intel_hum):
    """Fit linear model: humidity = a + b*temperature + noise.
    
    Returns (a, b, noise_std).
    """
    # Use numpy polyfit (degree 1)
    mask = np.isfinite(intel_temp) & np.isfinite(intel_hum)
    t = intel_temp[mask]
    h = intel_hum[mask]
    b, a = np.polyfit(t, h, 1)
    residuals = h - (a + b * t)
    noise_std = float(np.std(residuals))
    return float(a), float(b), noise_std


def calibrate_light_model(intel_light):
    """Fit a mixture model for light (bimodal: dark vs bright).
    
    Simple approach: two-component Gaussian mixture estimated via
    threshold splitting at the median.
    Returns (mu1, sig1, weight1, mu2, sig2).
    """
    arr = intel_light[np.isfinite(intel_light)]
    # Use 100 lux threshold to split dark/bright modes
    threshold = 100.0
    dark = arr[arr <= threshold]
    bright = arr[arr > threshold]
    w_dark = len(dark) / len(arr)
    mu1 = float(np.mean(dark)) if len(dark) > 0 else 10.0
    sig1 = float(np.std(dark)) if len(dark) > 1 else 30.0
    mu2 = float(np.mean(bright)) if len(bright) > 0 else 500.0
    sig2 = float(np.std(bright)) if len(bright) > 1 else 200.0
    return mu1, sig1, w_dark, mu2, sig2


def generate_calibrated_mvou_temp_hum(n, motes, theta_T, sigma_T,
                                       a_hum, b_hum, noise_hum,
                                       intel_temp, rho=-0.38):
    """Calibrated MVOU temperature-humidity with Cholesky-correlated noise."""
    global_mean = float(np.mean(intel_temp))
    global_std = float(np.std(intel_temp))
    dt = 1.0
    theta_H = 0.001
    sigma_H = noise_hum * 0.05

    T_vals = np.zeros(n)
    H_vals = np.zeros(n)
    per_mote = n // motes

    for m_idx in range(motes):
        start = m_idx * per_mote
        end = min(start + per_mote, n)
        steps = end - start

        mu_T = random.gauss(global_mean, global_std * 0.4)
        mu_H = a_hum + b_hum * mu_T

        current_T = mu_T + random.gauss(0, 1)
        current_H = mu_H + random.gauss(0, 3)

        dW1 = np.random.normal(0, math.sqrt(dt), steps)
        dW2 = np.random.normal(0, math.sqrt(dt), steps)
        dZ_T = dW1
        dZ_H = rho * dW1 + math.sqrt(1 - rho**2) * dW2

        for i in range(steps):
            dT = theta_T * (mu_T - current_T) * dt + sigma_T * dZ_T[i]
            max_rate = 0.5 / 60
            dT = max(-max_rate * dt, min(max_rate * dt, dT))
            current_T += dT

            target_H = a_hum + b_hum * current_T
            current_H += theta_H * (target_H - current_H) * dt + sigma_H * dZ_H[i]
            current_H = max(0.0, min(100.0, current_H))

            T_vals[start + i] = current_T
            H_vals[start + i] = current_H

    return T_vals, H_vals


