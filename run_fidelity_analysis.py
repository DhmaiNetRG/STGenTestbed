def plot_distribution_comparison(real, synth, modality, unit, outpath):
    """Side-by-side histogram + Q-Q plot."""
    fig, axes = plt.subplots(1, 3, figsize=(15, 4.5))

    # --- Histogram ---
    lo, hi = np.percentile(np.concatenate([real, synth]), [0.5, 99.5])
    bins = np.linspace(lo, hi, HIST_BINS)
    axes[0].hist(real, bins=bins, density=True, alpha=0.6, label="Intel Lab", color="#2196F3")
    axes[0].hist(synth, bins=bins, density=True, alpha=0.6, label="STGen", color="#FF5722")
    axes[0].set_xlabel(f"{modality} ({unit})")
    axes[0].set_ylabel("Density")
    axes[0].set_title(f"{modality} Distribution")
    axes[0].legend()

    # --- CDF ---
    real_sorted = np.sort(real)
    synth_sorted = np.sort(synth)
    axes[1].plot(real_sorted, np.linspace(0, 1, len(real_sorted)), label="Intel Lab", color="#2196F3")
    axes[1].plot(synth_sorted, np.linspace(0, 1, len(synth_sorted)), label="STGen", color="#FF5722")
    axes[1].set_xlabel(f"{modality} ({unit})")
    axes[1].set_ylabel("CDF")
    axes[1].set_title(f"{modality} ECDF")
    axes[1].legend()

    # --- Q-Q plot ---
    # subsample for speed
    n_qq = min(5000, len(real), len(synth))
    qq_real = np.sort(np.random.choice(real, n_qq, replace=False))
    qq_synth = np.sort(np.random.choice(synth, n_qq, replace=False))
    # Align lengths by interpolating quantiles
    quantiles = np.linspace(0, 1, n_qq)
    real_q = np.quantile(real, quantiles)
    synth_q = np.quantile(synth, quantiles)
    axes[2].scatter(real_q, synth_q, s=4, alpha=0.5, color="#4CAF50")
    diag_min = min(real_q.min(), synth_q.min())
    diag_max = max(real_q.max(), synth_q.max())
    axes[2].plot([diag_min, diag_max], [diag_min, diag_max], "k--", lw=1)
    axes[2].set_xlabel(f"Intel Lab quantiles")
    axes[2].set_ylabel(f"STGen quantiles")
    axes[2].set_title(f"{modality} Q-Q Plot")

    fig.tight_layout()
    fig.savefig(outpath, dpi=150, bbox_inches="tight")
    plt.close(fig)


