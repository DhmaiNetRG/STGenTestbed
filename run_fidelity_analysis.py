def compute_moments(arr: np.ndarray) -> dict:
    """Return mean, std, skewness, kurtosis."""
    return {
        "mean": float(np.mean(arr)),
        "std": float(np.std(arr, ddof=1)),
        "skew": float(sp_stats.skew(arr)),
        "kurtosis": float(sp_stats.kurtosis(arr)),  # excess kurtosis
        "median": float(np.median(arr)),
        "q25": float(np.percentile(arr, 25)),
        "q75": float(np.percentile(arr, 75)),
    }


def ks_test(real: np.ndarray, synth: np.ndarray) -> dict:
    """Two-sample Kolmogorov-Smirnov test."""
    d, p = sp_stats.ks_2samp(real, synth)
    return {"D": float(d), "p": float(p)}


def js_divergence(real: np.ndarray, synth: np.ndarray, bins: int = HIST_BINS) -> float:
    """Jensen-Shannon divergence between two empirical distributions."""
    lo = min(real.min(), synth.min())
    hi = max(real.max(), synth.max())
    edges = np.linspace(lo, hi, bins + 1)
    p, _ = np.histogram(real, bins=edges, density=True)
    q, _ = np.histogram(synth, bins=edges, density=True)
    # Add tiny epsilon for numerical stability
    p = p + 1e-12
    q = q + 1e-12
    p = p / p.sum()
    q = q / q.sum()
    return float(jensenshannon(p, q) ** 2)  # squared = actual JSD


def compute_acf_lags(series: np.ndarray, lags: list) -> dict:
    """Compute ACF at specific lags."""
    if len(series) < MAX_ACF_LAG + 10:
        return {l: float("nan") for l in lags}
    acf_vals = acf(series, nlags=MAX_ACF_LAG, fft=True)
    return {l: float(acf_vals[l]) for l in lags if l < len(acf_vals)}


def compute_cross_corr(x: np.ndarray, y: np.ndarray) -> float:
    """Pearson r between two equal-length arrays."""
    if len(x) != len(y):
        n = min(len(x), len(y))
        x, y = x[:n], y[:n]
    r, _ = sp_stats.pearsonr(x, y)
    return float(r)


