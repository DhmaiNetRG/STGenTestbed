        dZ_H_cal = rho_cal * dW1_cal + math.sqrt(1 - rho_cal**2) * dW2_cal
        for i in range(STGEN_DURATION_STEPS):
            dT = theta_cal * (mu_cal - ct) + sigma_cal * dZ_T_cal[i]
            max_rate = 0.5 / 60
            dT = max(-max_rate, min(max_rate, dT))
            ct += dT
            target_h = a_hum + b_hum * ct
            ch += theta_H_cal * (target_h - ch) + sigma_H_cal * dZ_H_cal[i]
            ch = max(0.0, min(100.0, ch))
            cal_temp_series[i] = ct
            cal_hum_series[i] = ch
        cal_results["temperature"]["stgen_acf"] = compute_acf_lags(cal_temp_series, ACF_LAGS)
        cal_results["humidity"]["stgen_acf"] = compute_acf_lags(cal_hum_series, ACF_LAGS)

        # Light: OU with regime switching (calibrated)
        cal_light_series = np.zeros(STGEN_DURATION_STEPS)
        dark_state = True
        l_c = mu1_l
        for i in range(STGEN_DURATION_STEPS):
            if dark_state:
                if random.random() < 0.02:
                    dark_state = False
                target_l, sig_l = mu1_l, sig1_l * 0.15
            else:
                if random.random() < 0.012:
                    dark_state = True
                target_l, sig_l = mu2_l, sig2_l * 0.14
            l_c = _ou_step(l_c, target_l, theta=0.01, sigma=sig_l,
                           lo=0, hi=100000)
            cal_light_series[i] = l_c
        cal_results["light"]["stgen_acf"] = compute_acf_lags(cal_light_series, ACF_LAGS)

        # Voltage: OU with drift
        cal_volt_series = np.zeros(STGEN_DURATION_STEPS)
        v = volt_mu
        for i in range(STGEN_DURATION_STEPS):
            v_mean_step = volt_mu - 0.00001 * i
            v = _ou_step(v, v_mean_step, theta=0.001, sigma=volt_sig * 0.05,
                         lo=1.5, hi=3.5)
            cal_volt_series[i] = v
        cal_results["voltage"]["stgen_acf"] = compute_acf_lags(cal_volt_series, ACF_LAGS)

        # Copy Intel ACF from earlier results
        for mod in ["temperature", "humidity", "light", "voltage"]:
            cal_results[mod]["intel_acf"] = results[mod]["intel_acf"]

