def per_mote_cv(df: pd.DataFrame, col: str) -> dict:
    """Coefficient of variation across motes."""
    grouped = df.groupby("moteid")[col]
    means = grouped.mean()
    stds = grouped.std()
    cvs = (stds / means.abs().clip(lower=1e-6))
    return {
        "mean_cv": float(cvs.mean()),
        "std_cv": float(cvs.std()),
        "min_cv": float(cvs.min()),
        "max_cv": float(cvs.max()),
    }


