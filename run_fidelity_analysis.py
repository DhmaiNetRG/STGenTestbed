def generate_mvou_temp_hum(n: int, motes: int = 54) -> Tuple[np.ndarray, np.ndarray]:
    """
    Multivariate Ornstein-Uhlenbeck (MVOU) process for Temp & Humidity.
    Uses Cholesky decomposition to correlate the stochastic noise terms.
    """
    dt = 1.0
    theta_T, sigma_T = 0.002, 0.15
    theta_H, sigma_H = 0.001, 0.20
    rho = -0.38  # Tuned: structural coupling + Cholesky ≈ -0.43 target

    T_vals = np.zeros(n)
    H_vals = np.zeros(n)
    per_mote = n // motes

    for m in range(motes):
        start = m * per_mote
        end = min(start + per_mote, n)
        steps = end - start

        # Mote-specific long-term baselines
        mu_T = random.gauss(22.2, 1.0)
        mu_T = max(18, min(30, mu_T))
        mu_H = 53.8 - 0.64 * mu_T

        current_T = mu_T + random.gauss(0, 1)
        current_H = mu_H + random.gauss(0, 3)

        # Generate independent Wiener processes
        dW1 = np.random.normal(0, math.sqrt(dt), steps)
        dW2 = np.random.normal(0, math.sqrt(dt), steps)

        # Cholesky decomposition to create correlated noise
        dZ_T = dW1
        dZ_H = rho * dW1 + math.sqrt(1 - rho**2) * dW2

        for i in range(steps):
            # Regime switching (HVAC/Sunlight) for heavy-tailed variance
            if random.random() < 0.001:
                mu_T += random.gauss(0, 4)
                mu_T = max(15, min(40, mu_T))
                mu_H = 53.8 - 0.64 * mu_T

            # SDE updates using correlated noise
            current_T += theta_T * (mu_T - current_T) * dt + sigma_T * dZ_T[i]
            current_H += theta_H * (mu_H - current_H) * dt + sigma_H * dZ_H[i]

            current_H = max(0.0, min(100.0, current_H))

            T_vals[start + i] = current_T
            H_vals[start + i] = current_H

    return T_vals, H_vals


def generate_stgen_light(n: int, motes: int = 54) -> np.ndarray:
    """Light: bimodal OU with regime switching (updated model).
    
    Dark regime: OU around μ=28, Bright regime: OU around μ=639.
    """
    per_mote = n // motes
    vals = np.zeros(n)
    for m in range(motes):
        start = m * per_mote
        end = start + per_mote
        bright = random.random() > 0.375  # 37.5% start dark
        current = 639.0 if bright else 28.0
        for i in range(start, min(end, n)):
            if bright:
                if random.random() < 0.012:
                    bright = False
                target, sigma_l = 639.0, 80.0
            else:
                if random.random() < 0.02:
                    bright = True
                target, sigma_l = 28.0, 5.0
            current = _ou_step(current, target, theta=0.01, sigma=sigma_l,
                               lo=0, hi=100000)
            vals[i] = current
    return vals


def generate_stgen_voltage(n: int, motes: int = 54) -> np.ndarray:
    """Voltage: OU process with slow discharge drift (updated model).
    
    Calibrated: μ≈2.56V, σ≈0.12V for Mica2Dot Li-ion.
    """
    per_mote = n // motes
    vals = np.zeros(n)
    for m in range(motes):
        start = m * per_mote
        end = start + per_mote
        v_mean = random.gauss(2.56, 0.08)
        current = v_mean
        for i in range(start, min(end, n)):
            v_mean -= 0.00001  # slow discharge
            current = _ou_step(current, v_mean, theta=0.001, sigma=0.005,
                               lo=1.5, hi=3.5)
            vals[i] = current
    return vals


