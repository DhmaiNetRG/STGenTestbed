def compute_iat_stats(df: pd.DataFrame) -> dict:
    """Compute inter-arrival time statistics from Intel dataset."""
    # Combine date+time into seconds
    times = pd.to_datetime(df["date"] + " " + df["time"], format="mixed", errors="coerce")
    iats_all = []
    for mote_id in df["moteid"].unique():
        mask = df["moteid"] == mote_id
        mote_times = times[mask].dropna().sort_values()
        if len(mote_times) < 2:
            continue
        diffs = mote_times.diff().dt.total_seconds().dropna()
        # Filter to plausible range (1s to 300s) — remove outlier gaps
        diffs = diffs[(diffs > 1) & (diffs < 300)]
        iats_all.extend(diffs.tolist())
    arr = np.array(iats_all)
    return {
        "mean": float(np.mean(arr)),
        "std": float(np.std(arr)),
        "median": float(np.median(arr)),
        "values": arr,
    }


