        # Calibrated ACF
        print("\n  Calibrated ACF at key lags:")
        for mod in ["temperature", "humidity", "light", "voltage"]:
            r = cal_results[mod]
            name = mod.capitalize().ljust(13)
            intel_acf_str = "  ".join([f"{r['intel_acf'].get(l, float('nan')):5.3f}" for l in ACF_LAGS])
            stgen_acf_str = "  ".join([f"{r['stgen_acf'].get(l, float('nan')):5.3f}" for l in ACF_LAGS])
            print(f"    {name}  Intel: {intel_acf_str}   STGen-Cal: {stgen_acf_str}")

        print(f"\n  Calibrated Temp–Humidity r: Intel={cal_cross_corr['intel']:.4f}, "
              f"STGen-Cal={cal_cross_corr['stgen_cal']:.4f}")

        # Calibrated fidelity scores
        print("\n  CALIBRATED FIDELITY SCORECARD:")
        cal_scores = []
        for mod in ["temperature", "humidity", "light", "voltage"]:
            r = cal_results[mod]
            ks_score = 1.0 - r["ks"]["D"]
            jsd_score = 1.0 - min(r["jsd"], 1.0)
            acf_diffs = []
            for l in ACF_LAGS:
                i_acf = r["intel_acf"].get(l, 0)
                s_acf = r["stgen_acf"].get(l, 0)
                if not (math.isnan(i_acf) or math.isnan(s_acf)):
                    acf_diffs.append(abs(i_acf - s_acf))
            acf_score = 1.0 - (np.mean(acf_diffs) if acf_diffs else 0.5)
            composite = (ks_score + jsd_score + acf_score) / 3.0
            cal_scores.append(composite)
            print(f"    {mod.capitalize():13s}:  KS={ks_score:.3f}  JSD={jsd_score:.3f}  ACF={acf_score:.3f}  → Composite={composite:.3f}")

        print(f"\n  Overall Fidelity Score (Calibrated): {np.mean(cal_scores):.3f} / 1.000")
        improvement = (np.mean(cal_scores) - np.mean(overall_scores)) / np.mean(overall_scores) * 100
        print(f"  Improvement over default: +{improvement:.1f}%")

